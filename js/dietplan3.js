function init(){
	var mealsSelection = JSON.parse(localStorage.getItem('dietplan3'));

	var html = '';

	var mealsTypes = ['breakfast', 'lunch', 'dinner', 'snack'];

	for (var i = 0; i < mealsTypes.length; i++) {
		var type = mealsTypes[i];
		var mealOption = mealsSelection[type];
		var meal = dietplanMeals[type][mealOption];
		var mealType = type.charAt(0).toUpperCase()+type.substr(1);
		html += '<h4>'+ mealType+': '+ meal.title+'</h4><p>'+meal.text+'</p>';
	}

	document.getElementById('dietMeals').innerHTML = html;

};

document.addEventListener("DOMContentLoaded", init);
